/// Build the `profile.conf` body (pure): the canonical YAML `profile: <p>` form.
pub fn profile_conf_body(profile: &str) -> String {
    format!("profile: {profile}\n")
}

/// Build the `pairing.json` body (pure). Mirrors `write_pairing`: the code is
/// uppercased and stamped with the creation epoch.
pub fn pairing_json(code: &str, created_at: u64) -> String {
    let upper = code.to_ascii_uppercase();
    format!("{{\n  \"pairing_code\": \"{upper}\",\n  \"code_created_at\": {created_at}\n}}\n")
}

/// Mint or read the stable 12-hex device id. Never overwrites an existing id
/// (the running agent's identity is derived from it). Prefers the kernel UUID
/// source, falling back to `getrandom` so the mint works on any host.
fn ensure_device_id() -> anyhow::Result<String> {
    let path = Path::new(DEVICE_ID_FILE);
    if let Ok(existing) = std::fs::read_to_string(path) {
        let trimmed = existing.trim();
        if !trimmed.is_empty() {
            tracing::info!(device_id = %trimmed, "device identity exists");
            return Ok(trimmed.to_string());
        }
    }
    let id = mint_device_id();
    if let Some(parent) = path.parent() {
        std::fs::create_dir_all(parent)?;
    }
    std::fs::write(path, format!("{id}\n"))?;
    set_mode(path, 0o644);
    tracing::info!(device_id = %id, "device identity generated");
    Ok(id)
}

/// Generate a fresh 12-hex device id. Reads `/proc/sys/kernel/random/uuid`
/// when present (matching the bash), else fills 6 bytes from `getrandom`.
fn mint_device_id() -> String {
    if let Ok(uuid) = std::fs::read_to_string("/proc/sys/kernel/random/uuid") {
        let hex: String = uuid
            .chars()
            .filter(|c| c.is_ascii_hexdigit())
            .take(12)
            .collect();
        if hex.len() == 12 {
            return hex.to_ascii_lowercase();
        }
    }
    let mut bytes = [0u8; 6];
    if getrandom::getrandom(&mut bytes).is_ok() {
        return hex::encode(bytes);
    }
    // Last-ditch deterministic-ish fallback (should never be reached): derive
    // from the process id + a fixed salt so we still produce 12 hex chars.
    let pid = std::process::id();
    format!("{pid:012x}").chars().take(12).collect()
}

/// Auto-detect the FC serial port. Mirrors the bash glob order
/// (`/dev/ttyACM* /dev/ttyAMA* /dev/ttyUSB*`), returning the first node found
/// or an empty string. The agent re-detects at runtime; this is only the seed.
fn detect_fc_port() -> String {
    for prefix in ["/dev/ttyACM", "/dev/ttyAMA", "/dev/ttyUSB"] {
        let dir = Path::new("/dev");
        if let Ok(read) = std::fs::read_dir(dir) {
            let base = prefix.trim_start_matches("/dev/");
            let mut matches: Vec<String> = read
                .flatten()
                .filter_map(|e| e.file_name().into_string().ok())
                .filter(|n| n.starts_with(base) && n.len() > base.len())
                .collect();
            matches.sort();
            if let Some(first) = matches.first() {
                return format!("/dev/{first}");
            }
        }
    }
    String::new()
}

/// Write the default config (skip-if-exists, matching `generate_default_config`).
/// A pre-existing config is left intact (an operator may have edited it / an
/// `--upgrade` preserves it); we only mint a fresh one on a clean box.
fn write_default_config(short_id: &str, name: Option<&str>, profile: &str) {
    let path = Path::new(CONFIG_YAML);
    if path.exists() {
        tracing::info!(
            path = CONFIG_YAML,
            "config already exists; leaving it intact"
        );
        return;
    }
    let agent_name = name
        .map(|n| n.to_string())
        .unwrap_or_else(|| format!("ados-{short_id}"));
    let fc_port = detect_fc_port();
    if !fc_port.is_empty() {
        tracing::info!(fc_port = %fc_port, "detected flight controller serial port");
    }
    let body = default_config_yaml(short_id, &agent_name, profile, &fc_port);
    if let Some(parent) = path.parent() {
        let _ = std::fs::create_dir_all(parent);
    }
    if let Err(e) = std::fs::write(path, body) {
        tracing::warn!(error = %e, "writing default config failed");
        return;
    }
    set_mode(path, 0o600);
    tracing::info!("default config written");
}

/// Persist the resolved profile to `/etc/ados/profile.conf` so it survives
/// reboots and a later `--upgrade`. Idempotent overwrite of the one-line file.
fn write_profile_conf(profile: &str) {
    let path = Path::new(PROFILE_CONF);
    if let Some(parent) = path.parent() {
        let _ = std::fs::create_dir_all(parent);
    }
    if let Err(e) = std::fs::write(path, profile_conf_body(profile)) {
        tracing::warn!(error = %e, "writing profile.conf failed");
    }
}

/// Set the system hostname from `--name` (slugified), rewrite the `/etc/hosts`
/// `127.0.1.1` line, and restart avahi so `<slug>.local` resolves. Best-effort
/// — a hostname failure must not abort the install.
fn set_hostname(name: &str) {
    let slug = slugify_hostname(name);
    if slug.is_empty() {
        tracing::warn!(
            name,
            "--name slugified to empty; leaving hostname unchanged"
        );
        return;
    }
    // hostnamectl is the canonical setter; fall back to /etc/hostname.
    if !exec::run_ok("hostnamectl", &["set-hostname", &slug]) {
        tracing::warn!(slug = %slug, "hostnamectl failed; writing /etc/hostname directly");
        let _ = std::fs::write("/etc/hostname", format!("{slug}\n"));
    } else {
        tracing::info!(slug = %slug, "hostname set");
    }
    rewrite_hosts_127_0_1_1(&slug);
    // Reconcile mDNS so <slug>.local resolves (best-effort).
    let _ = exec::run("systemctl", &["restart", "avahi-daemon"]);
}

/// Keep the `/etc/hosts` `127.0.1.1` line pointing at the new hostname so name
/// resolution does not stall. Rewrites that one line, or appends it.
fn rewrite_hosts_127_0_1_1(slug: &str) {
    let path = Path::new("/etc/hosts");
    let current = match std::fs::read_to_string(path) {
        Ok(c) => c,
        Err(_) => return,
    };
    let new = rewrite_hosts_body(&current, slug);
    if new != current {
        let _ = std::fs::write(path, new);
    }
}

/// Rewrite (or append) the `127.0.1.1 <slug>` line in a `/etc/hosts` body
/// (pure). Replaces an existing `127.0.1.1 ...` line; otherwise appends one.
pub fn rewrite_hosts_body(hosts: &str, slug: &str) -> String {
    let had_trailing_newline = hosts.ends_with('\n');
    let mut found = false;
    let mut out: Vec<String> = Vec::new();
    for raw in hosts.lines() {
        let starts_127011 = raw
            .split_whitespace()
            .next()
            .map(|t| t == "127.0.1.1")
            .unwrap_or(false);
        if starts_127011 {
            out.push(format!("127.0.1.1\t{slug}"));
            found = true;
        } else {
            out.push(raw.to_string());
        }
    }
    if !found {
        out.push(format!("127.0.1.1\t{slug}"));
    }
    let mut joined = out.join("\n");
    if had_trailing_newline || !found {
        joined.push('\n');
    }
    joined
}

/// Persist the pairing material from `--pair CODE`.
fn write_pairing(code: &str) {
    let path = Path::new(PAIRING_JSON);
    if let Some(parent) = path.parent() {
        let _ = std::fs::create_dir_all(parent);
    }
    let body = pairing_json(code, now_epoch());
    if let Err(e) = std::fs::write(path, body) {
        tracing::warn!(error = %e, "writing pairing.json failed");
        return;
    }
    set_mode(path, 0o600);
    tracing::info!(code = %code.to_ascii_uppercase(), "pairing code written");
}

/// Delegate the display + camera overlay provisioning to the brick-safe driver
/// scripts under the resolved source dir. Best-effort: a missing script or a
/// non-zero exit only degrades (the bash treats both as non-fatal).
///
/// Display default is profile-aware (the bash inverts the two): a ground
/// station auto-detects its panel; a drone defaults to `none`. Camera is the
/// inverse — a drone defaults to `auto`, a ground station to `none` — but the
/// camera auto-enable is still operator-gated, so without an explicit
/// `--camera` we pass `none` to match the bash safety gate.
fn provision_overlays(ctx: &Ctx, source: Option<&Path>) {
    let source = match source {
        Some(s) => s,
        None => {
            tracing::warn!("no source dir resolved; skipping overlay provisioning");
            return;
        }
    };

    // ── Display ──
    let display_script = source.join("scripts/drivers/install-display-overlay.sh");
    if display_script.is_file() {
        let display_arg = ctx.args.display.clone().unwrap_or_else(|| {
            if ctx.profile == "ground_station" {
                "auto".to_string()
            } else {
                "none".to_string()
            }
        });
        let script_s = display_script.to_string_lossy();
        tracing::info!(display = %display_arg, "running display overlay installer");
        if !exec::run_ok(&script_s, &["--display", &display_arg]) {
            tracing::warn!("display overlay install returned non-zero (best-effort)");
        }
    } else {
        tracing::warn!("display overlay installer not found; skipping display provisioning");
    }

    // ── Camera ── operator-gated auto-enable: only auto when --camera given.
    let camera_script = source.join("scripts/drivers/install-camera-overlay.sh");
    if camera_script.is_file() {
        let camera_arg = ctx
            .args
            .camera
            .clone()
            .unwrap_or_else(|| "none".to_string());
        let script_s = camera_script.to_string_lossy();
        tracing::info!(camera = %camera_arg, "running camera overlay installer");
        if !exec::run_ok(&script_s, &["--camera", &camera_arg]) {
            tracing::warn!("camera overlay install returned non-zero (best-effort)");
        }
    } else {
        tracing::warn!("camera overlay installer not found; skipping camera provisioning");
    }
}

/// Operator-facing note explaining how to revert the radio services to the
/// packaged Python implementations. Deployed at `/etc/ados/CUTOVER`.
const CUTOVER_NOTE_PATH: &str = "/etc/ados/CUTOVER";

/// The body of the CUTOVER note (pure). The WFB transmit plane and the
/// ground direct-role receive plane are native-only (their packaged
/// predecessor was deleted). Only the mesh relay and receiver roles keep a
/// packaged fallback behind a flag file; the note tells an operator how to
/// flip those back.
pub fn cutover_note_body() -> &'static str {
    "ADOS radio services run native binaries.\n\
\n\
The drone transmit plane and the ground direct-role receive plane are\n\
native-only — there is no Python fallback for them.\n\
\n\
The mesh relay and receiver roles keep a packaged Python implementation as\n\
an emergency fallback. To revert a mesh role to Python, create the fallback\n\
flag and restart its unit:\n\
\n\
  touch /etc/ados/groundlink-python-fallback\n\
  systemctl restart ados-wfb-relay ados-wfb-receiver\n\
\n\
Remove the flag file and restart the unit to return to the native binary.\n"
}

/// Deploy the operator CUTOVER note at `/etc/ados/CUTOVER`. Idempotent overwrite
/// — the text is stable across reinstalls. Best-effort: a write failure only
/// loses the note, it never aborts the install.
fn write_cutover_note() {
    let path = Path::new(CUTOVER_NOTE_PATH);
    if let Some(parent) = path.parent() {
        let _ = std::fs::create_dir_all(parent);
    }
    if let Err(e) = std::fs::write(path, cutover_note_body()) {
        tracing::warn!(error = %e, "writing CUTOVER note failed");
        return;
    }
    set_mode(path, 0o644);
    tracing::info!("radio cutover note written");
}

/// Plugin trust keys dir under /etc/ados.
const PLUGIN_KEYS_DIR: &str = "/etc/ados/plugin-keys";
/// Peripheral seed manifests dir under /etc/ados.
const PERIPHERALS_DIR: &str = "/etc/ados/peripherals";

/// Provision the first-party plugin trust keys at `/etc/ados/plugin-keys/` so
/// the agent can verify signed `.adosplug` archives against the
/// `FIRST_PARTY_SIGNERS` allowlist. Ports 09-config.sh `provision_plugin_keys`:
/// copy every `*.pem` from `<source>/scripts/plugin-keys/` (the keys ship in the
/// repo and the Rust installer clones to `/opt/ados/source`, so they are always
/// on disk). Idempotent overwrite — the key bytes are stable across reinstalls.
///
/// This closes an install/uninstall asymmetry: the dir was created but the keys
/// were never copied, so a fresh Rust install could not verify any signed plugin.
fn provision_plugin_keys(source: Option<&Path>) {
    let _ = std::fs::create_dir_all(PLUGIN_KEYS_DIR);
    set_mode(Path::new(PLUGIN_KEYS_DIR), 0o700);
    let src_dir = match source.map(|s| s.join("scripts/plugin-keys")) {
        Some(p) if p.is_dir() => p,
        _ => {
            tracing::warn!("plugin-keys source dir not found; skipping trust-key provisioning");
            return;
        }
    };
    let read = match std::fs::read_dir(&src_dir) {
        Ok(r) => r,
        Err(_) => return,
    };
    let mut copied = 0usize;
    for entry in read.flatten() {
        let path = entry.path();
        let name = match path.file_name().and_then(|n| n.to_str()) {
            Some(n) if n.ends_with(".pem") => n.to_string(),
            _ => continue,
        };
        let dst = Path::new(PLUGIN_KEYS_DIR).join(&name);
        if std::fs::copy(&path, &dst).is_ok() {
            set_mode(&dst, 0o600);
            copied += 1;
        }
    }
    tracing::info!(count = copied, "first-party plugin trust keys provisioned");
}

/// Seed the default BOM peripheral manifests at `/etc/ados/peripherals/` so the
/// webapp Peripherals page renders the FC, GPS, radio, OLED, SPI LCD, and camera
/// entries on a fresh board. Ports 09-config.sh `seed_default_peripherals`:
/// copy every `*.yaml` from `<source>/scripts/peripherals-seed/`, but PRESERVE
/// operator-edited files — a target is only overwritten when it is clearly one of
/// our shipped seeds (`id: ados.` on the first matching line) and the bytes
/// differ. Operator-added manifests (any non-`ados.` id) are never trampled.
fn seed_default_peripherals(source: Option<&Path>) {
    let _ = std::fs::create_dir_all(PERIPHERALS_DIR);
    set_mode(Path::new(PERIPHERALS_DIR), 0o755);
    let src_dir = match source.map(|s| s.join("scripts/peripherals-seed")) {
        Some(p) if p.is_dir() => p,
        _ => {
            tracing::warn!("peripherals-seed source dir not found; skipping seed manifests");
            return;
        }
    };
    let read = match std::fs::read_dir(&src_dir) {
        Ok(r) => r,
        Err(_) => return,
    };
    let mut copied = 0usize;
    let mut refreshed = 0usize;
    for entry in read.flatten() {
        let path = entry.path();
        let name = match path.file_name().and_then(|n| n.to_str()) {
            Some(n) if n.ends_with(".yaml") => n.to_string(),
            _ => continue,
        };
        let target = Path::new(PERIPHERALS_DIR).join(&name);
        let seed_bytes = match std::fs::read(&path) {
            Ok(b) => b,
            Err(_) => continue,
        };
        if !target.exists() {
            if std::fs::write(&target, &seed_bytes).is_ok() {
                set_mode(&target, 0o644);
                copied += 1;
            }
        } else if target_is_ados_seed(&target) {
            let existing = std::fs::read(&target).unwrap_or_default();
            if existing != seed_bytes && std::fs::write(&target, &seed_bytes).is_ok() {
                set_mode(&target, 0o644);
                refreshed += 1;
            }
        }
    }
    tracing::info!(copied, refreshed, "default peripheral manifests seeded");
}

/// True when a target manifest declares an `id: ados.` (our shipped seed) on a
/// non-comment line — the overwrite gate that preserves operator-added files.
/// Mirrors the bash `grep -q '^id:[[:space:]]*ados\.'`.
fn target_is_ados_seed(target: &Path) -> bool {
    let body = match std::fs::read_to_string(target) {
        Ok(b) => b,
        Err(_) => return false,
    };
    first_line_declares_ados_id(&body)
}

/// Pure: does any line declare `id: ados.<...>` (leading whitespace then `id:`,
/// optional whitespace, then the `ados.` prefix)? Mirrors the bash grep.
pub fn first_line_declares_ados_id(body: &str) -> bool {
    for raw in body.lines() {
        let line = raw.trim_start();
        if let Some(rest) = line.strip_prefix("id:") {
            return rest.trim_start().starts_with("ados.");
        }
    }
    false
}

/// Operator config + agent identity provisioning.
pub struct ConfigIdentity;

impl Step for ConfigIdentity {
    fn id(&self) -> &str {
        "config_identity"
    }
    fn requires(&self) -> &[&str] {
        &["venv_agent"]
    }
    fn checkpoint(&self) -> Option<&str> {
        None
    }
    fn kind(&self) -> StepKind {
        StepKind::Required
    }
    fn run(&self, ctx: &mut Ctx) -> StepOutcome {
        // 1. Device id (required: identity is the install's keystone).
        let device_id = match ensure_device_id() {
            Ok(id) => id,
            Err(e) => return StepOutcome::Failed(format!("could not mint device id: {e}")),
        };
        let short_id: String = device_id.chars().take(8).collect();

        // 2. Persist the resolved profile + the default config.
        write_profile_conf(&ctx.profile);
        write_default_config(&short_id, ctx.args.name.as_deref(), &ctx.profile);

        // 3. Hostname (only when --name given).
        if let Some(name) = ctx.args.name.clone() {
            set_hostname(&name);
        }

        // 4. Pairing material (only when --pair given).
        if let Some(code) = ctx.args.pair.clone() {
            write_pairing(&code);
        }

        // 5. Delegate the brick-safe overlay provisioning to the driver scripts.
        let source = env::resolve_source_dir(ctx.source_dir.as_deref());
        provision_overlays(ctx, source.as_deref());

        // 6. Provision the first-party plugin trust keys (so signed plugins can
        //    be verified) and seed the default peripheral manifests (so the
        //    Peripherals page renders on a fresh board). Both copy from the
        //    cloned source tree; idempotent + operator-state-preserving.
        provision_plugin_keys(source.as_deref());
        seed_default_peripherals(source.as_deref());

        // 7. Deploy the operator note describing how to revert the radio
        //    services to the packaged Python fallback.
        write_cutover_note();

        StepOutcome::Ok
    }
}

/// Seconds since the Unix epoch.
fn now_epoch() -> u64 {
    std::time::SystemTime::now()
        .duration_since(std::time::UNIX_EPOCH)
        .map(|d| d.as_secs())
        .unwrap_or(0)
}

/// chmod (Unix); a no-op on a non-Unix dev host.
#[cfg(unix)]
fn set_mode(path: &Path, mode: u32) {
    use std::os::unix::fs::PermissionsExt;
    let _ = std::fs::set_permissions(path, std::fs::Permissions::from_mode(mode));
}

#[cfg(not(unix))]
fn set_mode(_path: &Path, _mode: u32) {}

