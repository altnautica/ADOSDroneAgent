//! Config + identity: mint the device id, write `/etc/ados/profile.conf` +
//! `/etc/ados/config.yaml`, set the hostname (`--name`), persist the pairing
//! material (`--pair`), and delegate the display + camera overlay provisioning
//! to the battle-tested driver scripts. Required — the agent will not resolve
//! its profile or identity without it.
//!
//! Ports the pure file-write logic of `scripts/install.d/09-config.sh`
//! (`generate_device_id`, `generate_default_config`, `set_hostname`,
//! `write_pairing`) and delegates the two boot-config-editing overlay steps
//! (`install-display-overlay.sh` / `install-camera-overlay.sh`) via `exec` —
//! those carry brick-safe snapshot/restore logic that must not be re-ported.
//!
//! The slugify + the config-YAML builder are pure so a unit test exercises them
//! without touching the filesystem.

use std::path::Path;

use crate::ctx::Ctx;
use crate::env::{self, CONFIG_YAML, CONVEX_URL, DEVICE_ID_FILE, PAIRING_JSON, PROFILE_CONF};
use crate::exec;
use crate::graph::{Step, StepKind, StepOutcome};

/// Slugify a `--name` into a DNS-safe label: lowercase, `[a-z0-9-]` only,
/// runs of other chars collapse to a single `-`, leading/trailing `-`
/// trimmed, max 63 chars (then re-trim a trailing `-` the cut may expose).
/// Pure — mirrors the `sed` pipeline in `set_hostname`. Empty when nothing
/// usable survives.
pub fn slugify_hostname(name: &str) -> String {
    let mut out = String::with_capacity(name.len());
    let mut last_dash = false;
    for ch in name.chars() {
        let c = ch.to_ascii_lowercase();
        if c.is_ascii_lowercase() || c.is_ascii_digit() || c == '-' {
            if c == '-' {
                if last_dash {
                    continue;
                }
                last_dash = true;
            } else {
                last_dash = false;
            }
            out.push(c);
        } else if !last_dash {
            // A run of disallowed chars collapses to a single '-'.
            out.push('-');
            last_dash = true;
        }
    }
    let trimmed = out.trim_matches('-');
    let mut cut: String = trimmed.chars().take(63).collect();
    // The 63-char cut can leave a trailing '-'; trim it.
    while cut.ends_with('-') {
        cut.pop();
    }
    cut
}

